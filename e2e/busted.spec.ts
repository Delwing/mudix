import { test, expect } from '@playwright/test';
import { ALL_SPECS, loadManifest, specResults } from './bustedHarness';

// Mudlet's busted *_spec.lua suite, run against the real mudix app in a browser.
// This is the single path for the whole corpus: because the live app wires the
// full ScriptingEngine (trigger/alias dispatch, timer pump) and renders real
// overlay/Geyser geometry, specs the node thin-layer couldn't exercise (triggers
// via feedTriggers, DOM-dependent widgets) run here too.
//
// Each Mudlet it() is registered as its own Playwright test() so IDE runners
// (WebStorm/VS Code) and JUnit list every individual test as a first-class,
// re-runnable node — not just one row per spec. The test tree must be known at
// collection time (before any browser runs), so the it() names come from a
// committed manifest (e2e/busted.manifest.json) generated by genBustedManifest.ts.
// A per-spec drift guard fails loudly if the live it() set diverges from it.

const manifest = loadManifest();

// ── Per-test suite: one Playwright test() per Mudlet it() ────────────────────
for (const spec of ALL_SPECS) {
    const names = manifest[spec];
    if (!names || names.length === 0) continue; // manifest not generated yet

    test.describe(`${spec}_spec`, () => {
        names.forEach((name, idx) => {
            // Occurrence index disambiguates the rare case of two it()s sharing a
            // full name, so each test() title is unique and maps to the right run.
            const occ = names.slice(0, idx + 1).filter(n => n === name).length;
            const title = occ === 1 ? name : `${name} (#${occ})`;

            test(title, async ({ page }) => {
                const r = await specResults(page, spec);
                const t = r.tests.filter(x => x.name === name)[occ - 1];
                expect(
                    t,
                    `"${name}" not in live run — manifest drift; run \`npm run gen:busted-manifest\``,
                ).toBeTruthy();
                if (t.status === 'pending') {
                    test.skip(true, 'pending upstream stub');
                    return;
                }
                expect(t.status, t.message || name).toBe('success');
            });
        });
    });
}

// ── Drift guard: the manifest must match the live it() set, per spec ─────────
// Catches specs re-synced (or APIs changed) without regenerating the manifest:
// without this, an added/removed/renamed it() would silently never run. Also
// catches errors raised outside any it() (load/describe/setup failures), which
// never produce a test record and would otherwise dodge the per-test assertions.
test.describe('manifest drift guard', () => {
    for (const spec of ALL_SPECS) {
        test(`${spec}: manifest matches live it() set`, async ({ page }) => {
            const r = await specResults(page, spec);
            const live = r.tests.map(t => t.name).sort();
            const recorded = (manifest[spec] ?? []).slice().sort();
            expect(
                live,
                `${spec} it() set drifted from manifest — run \`npm run gen:busted-manifest\``,
            ).toEqual(recorded);
            expect(r.errors, `${spec}: ${r.errors} error(s) outside any it()`).toBe(0);
        });
    }
});
